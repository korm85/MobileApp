import fs from 'node:fs';
import path from 'node:path';
const root = path.resolve(new URL('..', import.meta.url).pathname);
const output = path.join(root, 'src', 'services', 'artifactRuntime.generated.ts');

function packageFile(packageName, relativePath) {
  return path.join(root, 'node_modules', packageName, relativePath);
}

const files = {
  THREE: packageFile('three', 'build/three.cjs'),
  echarts: packageFile('echarts', 'dist/echarts.min.js'),
  d3: packageFile('d3', 'dist/d3.min.js'),
  marked: packageFile('marked', 'lib/marked.umd.js'),
  DOMPurify: packageFile('dompurify', 'dist/purify.min.js'),
  Prism: packageFile('prismjs', 'prism.js'),
  katex: packageFile('katex', 'dist/katex.min.js'),
  Papa: packageFile('papaparse', 'papaparse.min.js'),
  JSZip: packageFile('jszip', 'dist/jszip.min.js'),
  Dexie: packageFile('dexie', 'dist/dexie.min.js'),
  Fuse: packageFile('fuse.js', 'dist/fuse.min.cjs'),
  Tweakpane: packageFile('tweakpane', 'dist/tweakpane.min.js'),
};

function asBrowserGlobal(name, code) {
  if (name === 'THREE' || name === 'Fuse') {
    return `/* ${name} */\n(function(){var module={exports:{}};var exports=module.exports;${code};window.${name}=module.exports;})();`;
  }
  if (name === 'Tweakpane') {
    const match = code.match(/export\{([\s\S]*?)\};\s*$/);
    if (!match) throw new Error('Could not convert Tweakpane ESM bundle to a browser global');
    const exports = match[1].split(',').map((entry) => {
      const [local, exported] = entry.trim().split(/\s+as\s+/);
      return `${JSON.stringify(exported ?? local)}:${local}`;
    }).join(',');
    return `/* ${name} */\n${code.slice(0, match.index)}window.${name}={${exports}};`;
  }
  return `/* ${name} */\n${code}`;
}

const libraries = Object.entries(files).map(([name, file]) => {
  if (!fs.existsSync(file)) throw new Error(`Missing artifact runtime file: ${file}`);
  const code = fs.readFileSync(file, 'utf8');
  return asBrowserGlobal(name, code);
}).join('\n;\n');

const source = `// Generated by scripts/generate-artifact-runtime.mjs.\nexport const ARTIFACT_RUNTIME = ${JSON.stringify(libraries)};\n`;
fs.mkdirSync(path.dirname(output), { recursive: true });
fs.writeFileSync(output, source);
console.log(`Generated ${output} (${Buffer.byteLength(source)} bytes).`);
